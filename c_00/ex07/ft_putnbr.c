#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c[100];
	int		count;

	count = 99;
	while (nb > 0)
	{
		c[count] = '0' + nb % 10;
		count--;
		nb = (int)(nb / 10);
	}
	write(1, &(c[count]), 100 - count);
}
