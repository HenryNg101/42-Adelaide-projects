char	*ft_strncpy(char *dest, char *src, int n)
{
	int	id;

	id = 0;
	while ((src[id] != '\0') && (id < n))
	{
		*(dest + id) = src[id];
		id++;
	}
	while (id < n)
	{
		dest[id] = '\0';
		id++;
	}
	return (dest);
}
