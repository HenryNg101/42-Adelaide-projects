char	*ft_strupcase(char *str)
{
	int	id;

	id = 0;
	while (str[id] != '\0')
	{
		if (str[id] >= 'a' && str[id] <= 'z')
		{
			str[id] -= ' ';
		}
		id++;
	}
	return (str);
}
