char	*ft_strlowcase(char *str)
{
	int	id;

	id = 0;
	while (str[id] != '\0')
	{
		if (str[id] >= 'A' && str[id] <= 'Z')
		{
			str[id] += ' ';
		}
		id++;
	}
	return (str);
}
