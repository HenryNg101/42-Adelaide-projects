int	ft_sqrt(int nb)
{
	int	check;

	check = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (nb > 0)
	{
		nb -= check;
		check += 2;
	}
	if (nb == 0)
	{
		return ((check - 1) / 2);
	}
	else
	{
		return (0);
	}
}
