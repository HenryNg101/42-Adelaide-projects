int	ft_is_prime(int nb)
{
	int	check;

	if (nb <= 1)
	{
		return (0);
	}
	check = 2;
	while (check <= (nb / 2))
	{
		if (nb % check == 0)
		{
			return (0);
		}
		check++;
	}
	return (1);
}
