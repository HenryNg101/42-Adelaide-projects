#include <unistd.h>

int	main(int argc, char **argv)
{
	int	id;

	id = argc;
	id = 0;
	while (argv[0][id] != '\0')
	{
		write(1, &argv[0][id], 1);
		id++;
	}
	write(1, "\n", 1);
	return (0);
}
